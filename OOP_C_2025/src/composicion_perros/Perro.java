package composicion_perros;

public class Perro {
	
	String nombre;
	String raza;
	double altura;
	double peso;
	String nacimiento;
	
	public Perro(String nombre, String raza, double altura, double peso, String nacimiento) {
		this.nombre = nombre;
		this.raza = raza;
		this.altura = altura;
		this.peso = peso;
		this.nacimiento = nacimiento;
	}

}